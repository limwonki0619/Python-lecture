# 22.5 리스트 표현식 사용하기 ---------------------------------------------------
a = [i for i in range(10)]  # 리스트 표현식에서는 뒤에서부터 실행
print(a)

a = [i for i in range(10) if i % 2 == 0]  # 0~9중 짝수만 출력
print(a)

b = [i + 5 for i in range(10) if i % 2 == 0] # 0~9중 짝수인 수에 5를 더한 값 출력
print(b)

c = [i * j for j in range(2, 10)
           for i in range(1, 10)]  # 구구단 출력
print(c)

# 22.6 리스트에 map 활용하기
# * list(map(함수, 리스트))
# * tuple(map(함수, 튜플))
a = [1.2, 2.5, 3.7, 4.6]
a = list(map(int, a))  # a 리스트를 정수로 변환 한 후 리스트로 다시 만들어주기
print(a)

a = list(map(int, input().split()))  # 정수로만 입력을 받음, input().split()의 결과는 문자열 리스트
print(a)

a = [10, 20]
a[-1:] = [40]  # -1 인덱스 위치의 값이 바뀜
print(a)


