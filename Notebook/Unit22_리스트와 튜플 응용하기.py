# 22.1 리스트 조작하기 -----------------------------------------------------
a = [10, 20, 30]
a.append(40)             # 요소 하나만 추가, 리스트의 길이가 항상 1씩 증가
a.extend([50, 60])       # 리스트 확장, 리스트 길이가 새로운 리스트 길이만큼 증가
a.insert(2, 500)         # 원하는 인덱스에 요소 추가, 2번 인덱스에 500요소 추가
a.insert(len(a), 500)    # 끝 인덱스에 500요소 추가
a.pop()                  # a의 마지막 요소 또는 특정 인덱스의 요소 삭제, 삭제되는 값 반환, 튜플에는 사용 불가
a.pop(2)                 # 2번 요소 삭제, 튜풀에는 사용불가
a.remove(60)             # 특정 값을 찾아서 삭제, 튜플에는 사용불가
a.index(20)              # 특정 값의 인덱스 반환
a.count(10)              # 특정 값의 개수 구하기
a.reverse()              # 리스트의 순서를 뒤집기
a.sort()                 # 리스트 정렬 메서드 : a의 내용을 변경하여 정렬
sorted(a)                # 리스트 정렬 함수 : a의 내용을 정렬하여 새 리스트 생성
a.clear()                # 리스트의 모든 요소 삭제

a = [10, 20, 30]
a[len(a):] = [500]       # 리스트 맨 끝에 500 요소 추가하기
print(a)

a[len(a):] = [600, 700]  # 리스트 끝에 [600, 700] 리스트 추가하기
print(a)                 # a.extend([500, 600])과 같음

# 22.2 리스트 할당과 복사 --------------------------------------------------
a = [0] * 5
b = a
print(b)
print(a is b)  # 실제로는 같은 객체에 할당

b[2] = 99
print(a, b)    # b의 2번 인덱스를 변경했지만, a도 같이 변경됨
               # 따라서 완전히 분리된 리스트를 만들기 위해선 copy 메서드를 이용

c = a.copy()   # 다른 객체에 할당이 되어 리스트 2개가 됨 (a와 c)
print(c)
print(a is c)
c[2] = 0
print(a, c)    # c는 2번째 인덱스가 변경되었지만, a는 변경되지 않음


# 22.3 반복문으로 리스트의 요소를 모두 출력하기 ----------------------------

a = [38, 21, 53, 62, 19]

for i in a:
    print(i)

for index, element in enumerate(a):
    print(index, element)

i = 0
while i < len(a):  # '<=' 를 사용할 경우 리스트의 범위를 벗어남 (len을 사용했기 대문)
    print(a[i])
    i += 1

